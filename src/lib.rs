//! # torchforge-viz
//!
//! Lightweight training visualizer with TensorBoard-compatible output for Rust ML pipelines.
//!
//! Writes training metrics — scalars, histograms, images — to TensorBoard event
//! files. No Python runtime required. Designed as a first-class, well-maintained
//! alternative to the abandoned `tensorboard-rs` crate.
//!
//! ## Status
//!
//! Active development. API is not yet stable.
